template<typename T>
vector<T> slicing(vector<T> const &v, int m, int n) {
   auto first = v.begin() + m;
   auto last = v.begin() + n + 1;
   vector<T> vector(first, last);
   return vector;
}

void solve()
{
    ll n, flg = 0, max1, max2;
    cin>>n;
    vector<ll> v(n);
    for(ll i=0; i<n; i++) cin>>v[i];
    vector<ll> a,b;
    ll ind=0;
    while(ind != n-1)
    {
        a = slicing(v,0,ind);
        b = slicing(v,ind+1,n-1);
        max1 = *max_element(a.begin(), a.end());
        max2 = *max_element(b.begin(), b.end());
        vector<ll> h1(max1+2,0), h2(max2+2,0);
        for(ll i=0; i<a.size(); i++)
            h1[a[i]]++;
        for(ll i=0; i<b.size(); i++)
            h2[b[i]]++;
        for(ll i=0; i<(min(max1,max2))+2; i++)
        {
            if(h1[i] == 0 && h2[i] == 0)
            {
                cout<<ind+1<<endl;
                flg = 1;
                break;
            }
        }
        if(flg == 1)
            break;
        ind++;
    }
    if(ind == n-1)
        cout<<"-1"<<endl;
}

